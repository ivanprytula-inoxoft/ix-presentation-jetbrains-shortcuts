"""03: Disable Tabs

Save space and stay keyboard-centric by turning off the tabs.

- Better ways to navigate code than lots-of-open-tabs

- Preferences | Editor | General | Editor Tabs | Placement -> None

- Or Find Action | tab pl no -> Enter

- My Personal Choice: Find Action, tab c -> Enter
    + Use small fonts for labels
    - Show file extension
    - Close button position -> None
    + Open new tabs at the end
"""


